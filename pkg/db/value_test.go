func TestMarshalMap(t *testing.T) {
	data, err := MarshalValue(map[int]string{1: "a", 2: "b"})
	assert.NoError(t, err)
	// map data order is not stable
	assert.Contains(
		t,
		[][]byte{
			{0x3, 0x17, 0x0, 0x0, 0x0, 0x2, 0x31, 0x0, 0x2, 0x0, 0x0, 0x0, 0x61, 0x0, 0x2, 0x32, 0x0, 0x2, 0x0, 0x0, 0x0, 0x62, 0x0, 0x0},
			{0x3, 0x17, 0x0, 0x0, 0x0, 0x2, 0x32, 0x0, 0x2, 0x0, 0x0, 0x0, 0x62, 0x0, 0x2, 0x31, 0x0, 0x2, 0x0, 0x0, 0x0, 0x61, 0x0, 0x0},
		},
		data,
		"data not match",
	)
}

func TestUnmarshalMap(t *testing.T) {
	var v map[int]string
	err := UnmarshalValue([]byte{0x3, 0x17, 0x0, 0x0, 0x0, 0x2, 0x31, 0x0, 0x2, 0x0, 0x0, 0x0, 0x61, 0x0, 0x2, 0x32, 0x0, 0x2, 0x0, 0x0, 0x0, 0x62, 0x0, 0x0}, &v)
	assert.NoError(t, err)
	assert.Equal(t, map[int]string{1: "a", 2: "b"}, v)
}
